# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer numero
# positivo ou negativo. int sem sinal é considerado positivo.
print(11) #int
print(-11) #int
print(0.0, -1.5) #int


# CTRL  K + CTRL  C -> Comenta as linhas selecionadas

# CTRL K + CTRL U -> Descomentar

# float -> Numero com ponto flutuante
# o tipo float representa qualquer número positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11) #float


#a função type mostra o tipo que o Python inferiu ao valor.
print(type('Pedro'))
print(type(2))
print(type(-2))
print(type(2.0), type(-1.1), type(0.0))