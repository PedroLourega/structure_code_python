# Tipo de dado bool(boolean)
# ao questionar algo em um programa só existem duas repostas possíveis: sim(True) ou não (False).
#Existem vários operadores para "questionar", dentre eles o ==, que é um operadoor lógico que questiona se um valor é igual a outro.

print(10 == 10) #Sim -> True (Verdadeiro)
print(10 == 11) #Nao -> False (Falso)
